#include "libft.h"

void		*ft_memset(void *b, int c, size_t n)
{
	char	*a;
	size_t	i;

	i = 0;
	a = (char *)b;
	while (i < n)
	{
		a[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
